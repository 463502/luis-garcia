    public void cargarTabla() {
        vaciarTabla();
        for (Jugador j : deportista) {
            JugadoresTabla.addRow(new Object[]{
                j.getNombre(),
                j.getApellido(),
                j.getApodo(),
                j.getNumero(),
                j.getEquipo(),
                j.getPosicion()
            });
        }
    }

    public void vaciarTabla() {
        for (int i = JugadoresTabla.getRowCount() - 1; i >= 0; i--) {
            JugadoresTabla.removeRow(i);
        }
    }

